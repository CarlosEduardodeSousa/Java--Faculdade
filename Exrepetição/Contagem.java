package Exrepetição;

public class Contagem {
    public static void main(String[] args) {
        int cont;

       for (cont = 0; cont <=51; cont++){
            System.out.println(cont);
       }

       for (cont = 52; cont <=100; cont = cont + 2){
            System.out.println(cont);
       }
    }
}
